#!/usr/bin/env node
/**
 * collect-results.js
 *
 * Reads the fresh Playwright JSON output (dashboard/results/latest.json),
 * appends a run record to dashboard/results/history.json,
 * then regenerates dashboard/data.js which the dashboard HTML reads.
 *
 * Usage: node scripts/collect-results.js
 */

const fs = require('fs');
const path = require('path');

const LATEST   = path.resolve(__dirname, '../dashboard/results/latest.json');
const HISTORY  = path.resolve(__dirname, '../dashboard/results/history.json');
const DATA_OUT = path.resolve(__dirname, '../dashboard/data.js');

// ─── Load latest Playwright JSON report ──────────────────────────────────

if (!fs.existsSync(LATEST)) {
  console.error('No latest.json found. Run: playwright test first.');
  process.exit(1);
}

const report = JSON.parse(fs.readFileSync(LATEST, 'utf8'));

// ─── Flatten to { suiteName, testTitle, status, duration, project } ──────

function flatten(suites, project = '') {
  const rows = [];
  for (const suite of suites ?? []) {
    const proj = project || suite.project || '';
    for (const spec of suite.specs ?? []) {
      for (const test of spec.tests ?? []) {
        const testProject = test.projectName || proj;
        rows.push({
          id: `${testProject}::${suite.title}::${spec.title}`,
          suite: suite.title,
          title: spec.title,
          project: testProject,
          status: test.results?.[0]?.status ?? 'unknown',
          duration: test.results?.[0]?.duration ?? 0,
        });
      }
    }
    // Recurse into nested suites
    rows.push(...flatten(suite.suites ?? [], proj));
  }
  return rows;
}

const tests = flatten(report.suites ?? []);

const runRecord = {
  runId: Date.now().toString(),
  timestamp: new Date().toISOString(),
  totalTests: tests.length,
  passed: tests.filter(t => t.status === 'passed').length,
  failed: tests.filter(t => t.status === 'failed').length,
  skipped: tests.filter(t => t.status === 'skipped').length,
  tests,
};

// ─── Append to history ────────────────────────────────────────────────────

const history = fs.existsSync(HISTORY)
  ? JSON.parse(fs.readFileSync(HISTORY, 'utf8'))
  : [];

history.push(runRecord);

// Keep last 50 runs
const trimmed = history.slice(-50);
fs.mkdirSync(path.dirname(HISTORY), { recursive: true });
fs.writeFileSync(HISTORY, JSON.stringify(trimmed, null, 2));

// ─── Write dashboard/data.js ──────────────────────────────────────────────

const js = `// Auto-generated by collect-results.js — do not edit manually
window.BAZAAR_TEST_HISTORY = ${JSON.stringify(trimmed, null, 2)};
`;

fs.writeFileSync(DATA_OUT, js);

console.log(`✅  Run recorded: ${runRecord.passed} passed, ${runRecord.failed} failed, ${runRecord.skipped} skipped`);
console.log(`📊  History: ${trimmed.length} runs stored`);
console.log(`💾  Dashboard data written to ${DATA_OUT}`);
